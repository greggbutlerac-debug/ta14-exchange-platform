export const CORPUS_COUNTS = TA14_PUBLIC_CORPUS.reduce<Record<CorpusCategory, number>>(
  (counts, record) => {
    counts[record.category] += 1;
    return counts;
  },
  {
    BOOK: 0,
    ARTICLE: 0,
    ZENODO: 0,
    PATENT: 0,
    STANDARD: 0,
    REPOSITORY: 0,
    SITE: 0,
    IMPLEMENTATION: 0,
    CHRONOLOGY: 0,
  },
);

export const CORPUS_TOTAL = TA14_PUBLIC_CORPUS.length;

export function getCorpusRecords(category?: CorpusCategory) {
  if (!category) return TA14_PUBLIC_CORPUS;
  return TA14_PUBLIC_CORPUS.filter((record) => record.category === category);
}

export function searchCorpus(query: string, category?: CorpusCategory) {
  const normalized = query.trim().toLowerCase();

  return getCorpusRecords(category).filter((record) => {
    if (!normalized) return true;

    return [
      record.title,
      record.date,
      record.year,
      record.author,
      record.platform,
      record.status,
      record.identifier,
      record.description,
      record.relationship,
      record.sourceClass,
      ...(record.tags ?? []),
    ]
      .filter(Boolean)
      .join(' ')
      .toLowerCase()
      .includes(normalized);
  });
}
